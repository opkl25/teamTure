package team.ture.vo;

public class TreplyVO {
	
	private int tridx;
	private String trwriter;
	private String trcontent;
	private String trwdate;
	private int midx;
	private int tidx;
	private int lidx;
	private String img;
	
	
	
	public String getImg() {
		return img;
	}
	public void setImg(String img) {
		this.img = img;
	}
	public int getTridx() {
		return tridx;
	}
	public void setTridx(int tridx) {
		this.tridx = tridx;
	}
	public String getTrwriter() {
		return trwriter;
	}
	public void setTrwriter(String trwriter) {
		this.trwriter = trwriter;
	}
	public String getTrcontent() {
		return trcontent;
	}
	public void setTrcontent(String trcontent) {
		this.trcontent = trcontent;
	}
	public String getTrwdate() {
		return trwdate;
	}
	public void setTrwdate(String trwdate) {
		this.trwdate = trwdate;
	}
	public int getMidx() {
		return midx;
	}
	public void setMidx(int midx) {
		this.midx = midx;
	}
	public int getTidx() {
		return tidx;
	}
	public void setTidx(int tidx) {
		this.tidx = tidx;
	}
	public int getLidx() {
		return lidx;
	}
	public void setLidx(int lidx) {
		this.lidx = lidx;
	}
	
	
	
}
